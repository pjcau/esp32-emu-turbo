def main():
    if not BOARD_CONFIG.exists():
        print(f"ERROR: {BOARD_CONFIG} not found", file=sys.stderr)
        sys.exit(1)

    defines = parse_defines(BOARD_CONFIG)
    print(f"Parsed {len(defines)} defines from {BOARD_CONFIG.name}")

    # Use v2 generator (cleaner, with helpers)
    # But ESP-IDF Unity uses TEST_CASE macro which auto-registers.
    # RUN_TEST() is not the right pattern for TEST_CASE.
    # With TEST_CASE, we need unity_run_all_tests() or just call each test fn.
    # Actually in ESP-IDF, the standard pattern is to call unity_run_menu()
    # or enumerate tests manually. Let's use a clean approach: define static
    # test functions and call them from app_main via a wrapper.

    content = generate_final(defines)

    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_FILE, "w") as f:
        f.write(content)

    line_count = content.count("\n") + 1
    test_count = content.count("static void test_")
    print(f"Generated {OUTPUT_FILE} ({line_count} lines, {test_count} tests)")


